// What this plugin does (Phase 4.1):
//   1. Watches network connectivity via ConnectivityManager.NetworkCallback
//      and pushes "wifi" / "cellular" / "offline" into the Rust core via
//      the `set_connectivity_state` Tauri command. The Rust auto-trigger
//      gate (`P2pConfig.wifi_only` + `ConnectivityState::allows_auto_sync`)
//      consumes this to suppress sync on metered cellular.
//
//   2. Schedules a 15-minute background sync via WorkManager.PeriodicWorkRequest
//      with NetworkType.UNMETERED. Android's minimum periodic interval is
//      15 minutes; setting it lower silently rounds up. The worker just
//      invokes the Tauri command `trigger_sync_now` — same code path
//      foreground sync uses.
//
//   3. Exposes a `connectivityType()` plugin command so the frontend can
//      query the current state for the Settings → Devices indicator.
